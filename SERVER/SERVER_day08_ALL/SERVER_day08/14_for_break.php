<?php
/*
练习：输出100以内的质数
*/

#思路：
#  外层循环$i:   2~99  $i要进行质数判定的数字
#  内层循环$j：  2/3/4...$i-1   依次试着去除$i，能整除说明$i不是素数；某个数字不能整除$i,继续判定下一个数是否能整除


for($i=2; $i<=99; $i++){   #$i是要进行质数判定的数字
	for($j=2; $j<$i; $j++){  #$j是除数
		if($i%$j===0){
			break;  //$i能被$j整除，退出内层循环，此时$j一定小于$i
		}
	}
	//已经退出内层循环，此时：
	//$i 和 $j 谁大？  $i:35  $j:5   => $i不是素数
	//$i 和 $j 谁大？  $i:7   $j:7   => $i是素数
	if($i===$j){  //输出一个素数
		echo $i."&nbsp;&nbsp;";
	}
}





















