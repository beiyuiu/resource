<?php
/*
学子商城中使用一个数字保存订单的状态：
	1-等待付款、2-备货中、3-运输中、4-派送中、5-订单完成、其他状态
*/
$status = 8;			

//根据上述变量的值，输出对应的提示文字  ——  使用三目运算
$result = $status===1 ? '等待付款' : (
			$status===2 ? '备货中' : (
			  $status===3 ? '运输中' : (
			    $status===4 ? '派送中' : (
				  $status===5 ? '订单完成' : '其他状态'
				)
			  )
			)
		  );

echo $result;